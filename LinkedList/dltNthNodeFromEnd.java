class Solution {
    public ListNode removeNthFromEnd(ListNode head, int n) {
        int size = 0;
        ListNode curr = head;
        while(curr != null){
            size++;
            curr = curr.next;
        }
        if(n==size){
            return head.next;
        }
        curr = head;
        for(int i=1;i<size-n;i++){
         curr = curr.next;
        }
        curr.next = curr.next.next;
         return head;
    }
}